void RF_Setup();
void RF_Send();
//--------------------------------------------------------------------------------------------------------
void RF_Setup(){
  //Wirtual wire setup   
  vw_set_tx_pin(Tx_pin);
  vw_set_rx_pin(-1);
  vw_set_ptt_pin(-1);        
  vw_setup(RF_rate);	 // Bits per sec
}
//--------------------------------------------------------------------------------------------------------
void RF_Send(){ 
  vw_send((uint8_t *)&Sensor_RF, sizeof(Sensor_RF));
  vw_wait_tx(); // Wait until the whole message is gone   
}
